package lv.javaguru.demo.HomeWork;

public class Dog {
    public int age;
    public String color;
    public String name;

    public Dog(){
        this.age = age;
        this.color = color;
        this.name = name;
    }

    public void voice() {
        System.out.println("When " + this.name + " barks, she says: Wow-wow!");
    }
    public void eat() {
        System.out.println("When " + this.name + " eat, she do: njam-njam!!");
    }
    public void sleep() {
        System.out.println("When " + this.name + " sleep, she do: hrrrrrr...");
    }
}
